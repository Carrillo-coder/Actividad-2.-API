package com.example.demo;

public class WeatherRequest {

    private String Lat;
    private String Lon;

    public void setLat(String lat) {
        this.Lat = lat;
    }

    public void setLon(String lon) {
        this.Lon = lon;
    }

    public String getLat() {
        return Lat;
    }

    public String getLon() {
        return Lon;
}


}
